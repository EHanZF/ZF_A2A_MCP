/**
 * Run the full test suite with coverage reporting
 */
function runTests(): { passed: number; failed: number; skipped: number; coverage: number } {
  try {
    // Run tests with coverage
    const output = execSync(
      "npm run test -- --run --coverage 2>&1 || true",
      { encoding: "utf-8" }
    );

    // Parse test output (simplified; adjust for your test framework)
    const passMatch = output.match(/(\d+) passed/);
    const failMatch = output.match(/(\d+) failed/);
    const skipMatch = output.match(/(\d+) skipped/);
    const coverMatch = output.match(/Statements\s*:\s*([\d.]+)%/);

    return {
      passed: parseInt(passMatch?.[1] || "0"),
      failed: parseInt(failMatch?.[1] || "0"),
      skipped: parseInt(skipMatch?.[1] || "0"),
      coverage: parseFloat(coverMatch?.[1] || "0"),
    };
  } catch (e) {
    console.error("Test execution failed:", e);
    return { passed: 0, failed: 0, skipped: 0, coverage: 0 };
  }
}

/**
 * Validate modified GitHub Actions workflows
 */
function validateWorkflows(): { issues: number; warnings: string[] } {
  const warnings: string[] = [];

  try {
    const output = execSync(
      "npm run lint -- --plugin actions 2>&1 || true",
      { encoding: "utf-8" }
    );

    if (output.includes("pull_request_target")) {
      warnings.push(
        "⚠️  Found pull_request_target in workflows — review for injection risks"
      );
    }
    if (output.includes("sudo")) {
      warnings.push("⚠️  Found sudo in workflows — review privilege escalation");
    }
    if (output.includes("::set-output")) {
      warnings.push(
        "⚠️  Deprecated ::set-output found — use $GITHUB_OUTPUT instead"
      );
    }
  } catch (e) {
    console.warn("Workflow validation tool not available:", e);
  }

  return {
    issues: warnings.length,
    warnings,
  };
}

/**
 * Create or update a pull request
 */
async function createPullRequest(
  branch: string,
  baseBranch: string,
  title: string,
  body: string
): Promise<string> {
  try {
    // Ensure branch is pushed
    execSync(`git push -u origin ${branch}`, { stdio: "inherit" });

    // Use GitHub CLI to create PR
    const prOutput = execSync(
      `gh pr create \
        --base ${baseBranch} \
        --head ${branch} \
        --title "${title}" \
        --body "${body}" \
        --draft`,
      { encoding: "utf-8" }
    );

    // Extract PR URL from output
    const urlMatch = prOutput.match(/https:\/\/github\.com\/.*?\/pull\/\d+/);
    return urlMatch ? urlMatch[0] : prOutput.trim();
  } catch (e) {
    console.error("Failed to create PR:", e);
    throw e;
  }
}

export { createPullRequest };

/**
 * Main fuzz branch skill entry point
 */
export async function fuzzBranchDiffs(
  input: FuzzBranchInput
): Promise<FuzzBranchOutput> {
  const branch = input.branch || execSync("git rev-parse --abbrev-ref HEAD", {
    encoding: "utf-8",
  }).trim();
  const baseBranch = input.baseBranch || "main";
  const generateTests = input.generateTests !== false;

  console.log(`[FUZZ] Starting branch fuzz on: ${branch} vs ${baseBranch}`);

  // Step 1: Get diff statistics
  const diffSummary = getDiffStats(branch, baseBranch);
  console.log(`[FUZZ] Diff summary:`, diffSummary);

  // Step 2: Generate fuzz tests (optional)
  let fuzzTestsGenerated = 0;
  if (generateTests && diffSummary.changedFiles.length > 0) {
    fuzzTestsGenerated = generateFuzzTests(diffSummary.changedFiles);
    console.log(`[FUZZ] Generated ${fuzzTestsGenerated} fuzz test files`);
  }

  // Step 3: Run full test suite
  const testResults = runTests();
  console.log(`[FUZZ] Test results:`, testResults);

  // Step 4: Validate workflows
  const workflowValidation = validateWorkflows();
  console.log(`[FUZZ] Workflow validation:`, workflowValidation);

  // Step 5: Determine PR status
  const allTestsPassed = testResults.failed === 0;
  const noWorkflowIssues = workflowValidation.issues === 0;
  let prStatus: "created" | "pending" | "failed" = "pending";
  let prUrl: string | undefined;

  if (allTestsPassed && noWorkflowIssues && input.autoCommit) {
    try {
      const prTitle =
        input.prTitle ||
        `[Agentic] Fuzz-tested branch: ${branch} (${diffSummary.filesChanged} files changed)`;
      const prBody =
        input.prBody ||
        `
## Automated Test Report

**Branch:** \`${branch}\`
**Base:** \`${baseBranch}\`

### Diff Summary
- Files Changed: ${diffSummary.filesChanged}
- Insertions: ${diffSummary.insertions}
- Deletions: ${diffSummary.deletions}

### Test Results
- ✅ Passed: ${testResults.passed}
- ❌ Failed: ${testResults.failed}
- ⏭️  Skipped: ${testResults.skipped}
- 📊 Coverage: ${testResults.coverage}%

### Fuzz Testing
- Generated: ${fuzzTestsGenerated} test files

### Workflow Validation
- Issues: ${workflowValidation.issues}
${workflowValidation.warnings.length > 0 ? "- Warnings:\n" + workflowValidation.warnings.map((w) => `  - ${w}`).join("\n") : ""}

---
*Generated by Agentic CI (branch.fuzz_and_test skill)*
`;

      prUrl = await createPullRequest(branch, baseBranch, prTitle, prBody);
      prStatus = "created";
      console.log(`[FUZZ] PR created: ${prUrl}`);
    } catch (e) {
      console.error("[FUZZ] Failed to create PR:", e);
      prStatus = "failed";
    }
  }

  const result: FuzzBranchOutput = {
    branch,
    baseBranch,
    diffSummary,
    fuzzTestsGenerated,
    testResults,
    workflowValidation,
    prStatus,
    prUrl,
    commit: branch,
  };

  console.log("[FUZZ] Skill execution complete:", result);
  return result;
}

export default fuzzBranchDiffs;
