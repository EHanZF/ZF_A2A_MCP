/**
 * fuzzBranchDiffs.ts
 *
 * CI Agent Skill: Fuzzes local branch against main, generates unit tests,
 * runs validation suite, and coordinates PR creation.
 *
 * This skill:
 * 1. Extracts diffs between current branch and main
 * 2. Generates targeted fuzz tests for changed code paths
 * 3. Runs the full test suite with coverage reporting
 * 4. Validates all workflows pass the linter
 * 5. Creates a PR if all checks pass
 */

import { execSync } from "child_process";
import fs from "fs";
import path from "path";

export interface FuzzBranchInput {
  branch?: string;          // local branch name (default: current)
  baseBranch?: string;      // base branch for comparison (default: main)
  generateTests?: boolean;  // auto-generate fuzz tests (default: true)
  autoCommit?: boolean;     // commit and push to PR (default: false)
  prTitle?: string;         // custom PR title
  prBody?: string;          // custom PR description
}

export interface FuzzBranchOutput {
  branch: string;
  baseBranch: string;
  diffSummary: {
    filesChanged: number;
    insertions: number;
    deletions: number;
    changedFiles: string[];
  };
  fuzzTestsGenerated: number;
  testResults: {
    passed: number;
    failed: number;
    skipped: number;
    coverage: number;
  };
  workflowValidation: {
    issues: number;
    warnings: string[];
  };
  prStatus: "created" | "pending" | "failed";
  prUrl?: string;
  commit?: string;
}

/**
 * Extract git diff statistics between branches
 */
function getDiffStats(branch: string, baseBranch: string): any {
  try {
    const diffOutput = execSync(
      `git diff --numstat ${baseBranch}...${branch}`,
      { encoding: "utf-8" }
    );

    let insertions = 0,
      deletions = 0;
    const changedFiles: string[] = [];

    diffOutput.split("\n").forEach((line) => {
      const [adds, removes, file] = line.split("\t");
      if (file) {
        changedFiles.push(file);
        insertions += parseInt(adds) || 0;
        deletions += parseInt(removes) || 0;
      }
    });

    return {
      filesChanged: changedFiles.length,
      insertions,
      deletions,
      changedFiles,
    };
  } catch (e) {
    console.error("Failed to get diff stats:", e);
    return {
      filesChanged: 0,
      insertions: 0,
      deletions: 0,
      changedFiles: [],
    };
  }
}

